function chatChannel() {
	if (Kakao.isInitialized()) {
		Kakao.Channel.chat({
			channelPublicId: '_xaUqtK'
		});
	} else {
		window.open('https://pf.kakao.com/_xaUqtK/chat', '_blank');
	}
}

